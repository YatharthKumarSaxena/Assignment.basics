// Q5:- WAP to find the circumference of a circle with radius 10 in C++
// Ans:- Code is written below:-
#include <iostream>
using namespace std;
int main(){
    int r = 10; //r is the radius of the circle
    float pi = 3.14159;
    cout<<"The circumference of the circle = "<<2*pi*r;
    return 0;
}